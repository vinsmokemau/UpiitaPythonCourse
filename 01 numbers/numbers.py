"""Data Types: Numbers."""

# Int types
print(type(7))
print(type(-28))

# Float types
print(type(-6.3))
print(type(1.0))

# Complex types
print(type(5 - 9j))
print(type(49j))

# Binary int types
print(type(0b100))

# Octal int types
print(type(0o101))

# Hexadecimal int types
print(type(0x110))


